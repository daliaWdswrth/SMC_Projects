


//loop Fizz for odd numbers
for (let i = 1; i <= 10; i++) {
    if (i % 2 != 0) {
        console.log("Fizz");
    }
    else {
        console.log(i);
    }
}

console.log("\n");

//loop Buzz for even numbers
for (let i = 10; i >= 1; i--) {
    if (i % 2 == 0) {
        console.log("Buzz");
    }
    else {
        console.log(i);
    }
}


